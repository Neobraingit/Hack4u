#!/usr/bin/env python3

name = 'Marcos'

'''---Manera simple----'''

print ('Hola, mi nombre es: ' + name)  # Esta es una manera simple de representar la cadena

'''---Porcentaje s----'''

print ('Hola, mi nombre es: %s', name) # Esta es la manera con el porcentaje

'''.format()'''

print ('Hola, soy {}'.format(name))

'''---f-strings----'''

print (f'Hola, soy {name}')